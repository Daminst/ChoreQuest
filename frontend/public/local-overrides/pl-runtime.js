  const ATTRIBUTES = ['placeholder', 'title', 'aria-label', 'alt'];
  const SKIP_SUBTREE_TAGS = new Set(['SCRIPT', 'STYLE', 'NOSCRIPT']);
  const SKIP_TEXT_TAGS = new Set(['SCRIPT', 'STYLE', 'NOSCRIPT', 'CODE', 'PRE', 'TEXTAREA']);

  function normalize(value) {
    return String(value || '').replace(/\s+/g, ' ').trim();
  }

  function translateClean(clean) {
    if (!clean) return null;
    if (TEXT.has(clean)) return TEXT.get(clean);
    for (const [pattern, replacement] of PATTERNS) {
      const match = clean.match(pattern);
      if (match) {
        return typeof replacement === 'function'
          ? replacement(...match)
          : clean.replace(pattern, replacement);
      }
    }
    return null;
  }

  function translateKnownTerm(value) {
    const clean = normalize(value);
    return TEXT.get(clean) || clean;
  }

  function translateRarity(value) {
    const rarities = {
      common: 'zwykły',
      uncommon: 'niepospolity',
      rare: 'rzadki',
      epic: 'epicki',
      legendary: 'legendarny'
    };
    return rarities[normalize(value)] || normalize(value);
  }

  function translateDialogMessage(message) {
    const clean = normalize(message);
    return translateClean(clean) || message;
  }

  function installDialogTranslations() {
    if (window.__CHOREQUEST_POLISH_DIALOGS__) return;
    window.__CHOREQUEST_POLISH_DIALOGS__ = true;

    const nativeAlert = window.alert.bind(window);
    const nativeConfirm = window.confirm.bind(window);
    const nativePrompt = window.prompt.bind(window);

    window.alert = (message) => nativeAlert(translateDialogMessage(message));
    window.confirm = (message) => nativeConfirm(translateDialogMessage(message));
    window.prompt = (message, defaultValue) => nativePrompt(translateDialogMessage(message), defaultValue);
  }

  function withOriginalWhitespace(original, translated) {
    const leading = original.match(/^\s*/)?.[0] || '';
    const trailing = original.match(/\s*$/)?.[0] || '';
    return `${leading}${translated}${trailing}`;
  }

  function adjacentTextNode(node, direction) {
    let current = direction === 'next' ? node.nextSibling : node.previousSibling;
    while (current) {
      if (current.nodeType === Node.TEXT_NODE) return current;
      if (current.nodeType === Node.ELEMENT_NODE && normalize(current.textContent)) return null;
      current = direction === 'next' ? current.nextSibling : current.previousSibling;
    }
    return null;
  }

  function translateTextNode(node) {
    const original = node.nodeValue;
    const clean = normalize(original);

    if (node.parentElement?.closest('.avatar-editor-shell, .fixed.inset-0')) {
      if (clean === 'Pet') {
        node.nodeValue = withOriginalWhitespace(original, 'Pupil');
        return;
      }
      if (clean === 'Custom') {
        node.nodeValue = withOriginalWhitespace(original, 'Własna');
        return;
      }
    }

    const splitQuestCount = clean.match(/^(\d+) quest$/);
    if (splitQuestCount) {
      const next = adjacentTextNode(node, 'next');
      if (next && normalize(next.nodeValue) === 's') {
        node.nodeValue = withOriginalWhitespace(original, `${splitQuestCount[1]} misji`);
        next.nodeValue = '';
        return;
      }
    }

    if (clean === 's') {
      const previous = adjacentTextNode(node, 'previous');
      const previousClean = previous ? normalize(previous.nodeValue) : '';
      const translatedCount = previousClean.match(/^(\d+) misja$/);
      const untranslatedCount = previousClean.match(/^(\d+) quest$/);
      if (translatedCount || untranslatedCount) {
        const count = (translatedCount || untranslatedCount)[1];
        previous.nodeValue = withOriginalWhitespace(previous.nodeValue, `${count} misji`);
        node.nodeValue = '';
        return;
      }
      if (previousClean === 'misja' || previousClean === 'quest') {
        const countNode = adjacentTextNode(previous, 'previous');
        const countMatch = countNode ? normalize(countNode.nodeValue).match(/^(\d+)$/) : null;
        if (countMatch) {
          countNode.nodeValue = withOriginalWhitespace(countNode.nodeValue, `${countMatch[1]} misji`);
          previous.nodeValue = '';
          node.nodeValue = '';
          return;
        }
      }
    }

    const translated = translateClean(clean);
    if (translated && translated !== clean) {
      node.nodeValue = withOriginalWhitespace(original, translated);
    }
  }

  function translateElement(element) {
    if (!element || SKIP_SUBTREE_TAGS.has(element.tagName)) return;

    for (const attr of ATTRIBUTES) {
      if (!element.hasAttribute(attr)) continue;
      const original = element.getAttribute(attr);
      const clean = normalize(original);
      const avatarTranslation = element.closest?.('.avatar-editor-shell, .fixed.inset-0')
        ? { Pet: 'Pupil', Custom: 'Własna' }[clean]
        : null;
      const translated = avatarTranslation || translateClean(clean);
      if (translated && translated !== clean) {
        element.setAttribute(attr, translated);
      }
    }

    if (
      element.tagName === 'INPUT' &&
      ['button', 'submit', 'reset'].includes((element.getAttribute('type') || '').toLowerCase())
    ) {
      const original = element.value;
      const clean = normalize(original);
      const translated = translateClean(clean);
      if (translated && translated !== clean) element.value = translated;
    }
  }

  function walk(root) {
    if (!root) return;
    if (root.nodeType === Node.TEXT_NODE) {
      translateTextNode(root);
      return;
    }
    if (root.nodeType !== Node.ELEMENT_NODE && root.nodeType !== Node.DOCUMENT_NODE) return;

    if (root.nodeType === Node.ELEMENT_NODE) translateElement(root);

    const walker = document.createTreeWalker(
      root,
      NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT,
      {
        acceptNode(node) {
          const parent = node.nodeType === Node.TEXT_NODE ? node.parentElement : node;
          if (node.nodeType === Node.TEXT_NODE && parent && SKIP_TEXT_TAGS.has(parent.tagName)) {
            return NodeFilter.FILTER_REJECT;
          }
          if (node.nodeType === Node.ELEMENT_NODE && SKIP_SUBTREE_TAGS.has(node.tagName)) {
            return NodeFilter.FILTER_REJECT;
          }
          return NodeFilter.FILTER_ACCEPT;
        }
      }
    );

    let node = walker.currentNode;
    while (node) {
      if (node.nodeType === Node.TEXT_NODE) translateTextNode(node);
      else translateElement(node);
      node = walker.nextNode();
    }
  }

  let scheduled = false;
  function schedule(root = document.body || document.documentElement) {
    if (scheduled) return;
    scheduled = true;
    requestAnimationFrame(() => {
      scheduled = false;
      document.documentElement.lang = 'pl';
      walk(root);
    });
  }

  function install() {
    document.documentElement.lang = 'pl';
    installDialogTranslations();
    walk(document.body || document.documentElement);

    const observer = new MutationObserver((mutations) => {
      for (const mutation of mutations) {
        if (mutation.type === 'attributes') {
          translateElement(mutation.target);
          continue;
        }
        if (mutation.type === 'characterData') {
          translateTextNode(mutation.target);
          continue;
        }
        if (mutation.addedNodes?.length) {
          for (const node of mutation.addedNodes) walk(node);
          continue;
        }
        schedule(mutation.target);
      }
    });

    observer.observe(document.documentElement, {
      childList: true,
      subtree: true,
      characterData: true,
      attributes: true,
      attributeFilter: [...ATTRIBUTES, 'value']
    });
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', install, { once: true });
  } else {
    install();
  }
